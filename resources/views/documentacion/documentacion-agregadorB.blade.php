<h5>c) Resolución mínima de la foto.</h5>

<p>
Las fotos que subamos deben tener una resolución mínima para poder trabajarlas. Esa resolución está en los píxeles de la imagen, que deben superar los 3 megapíxeles. 
Una forma sencilla de establecer la resolución es por el peso en bytes del fichero, siempre ha de ser superior a 700 K, aunque lo recomendable es que supere las 1.000 K, 
es decir, mayor de 1Mb. Los que tengáis Iphone normalmente podréis escoger el tamaño de fichero cuando estéis subiendo una foto. Tomad siempre la opción de tamaño real 
(opción preferible), o la siguiente siempre que supere el límite de 700 K de peso.
</p>

