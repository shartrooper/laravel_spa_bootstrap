<h4>ii) Procedimiento de subida de la información.</h4>

<ol>La subida de información tiene 3 partes fundamentales::

<li>El tipo de documento.</li>
<li>La forma de las imágenes.</li>
<li>La resolución mínima de las imágenes.</li>

</ol>

<h5>a) Tipo de Documento.</h5>

<p>

Tenemos tres opciones: 1) Documentos pdf directamente recibidos del sistema de origen (nóminas, impuestos, pensiones, Vida Laboral, etc.); 
son los mejores y los que garantizan una validación rápida y segura. 2) Documentos pdf originados al escanear el documento original en una máquina a propósito;
también son muy recomendables y de alta eficacia. 3) Fotos de los documentos originales impresos (nunca de pantallas de ordenador o móvil); 
son aceptables pero son los menos recomendables y habrá que asegurar que tienen una resolución mínima aceptable siguiendo las instrucciones del párrafo siguiente.

</p>

<h5>b) Forma de las imágenes si no tenemos pdf y subimos fotos de los documentos.</h5>

<p>
Si no disponemos de los pdf originales de los documentos electrónicos, o de pdf escaneados de los documentos originales impresos, 
podemos subir fotos de esos documentos impresos (nunca de pantallas de ordenador o móvil).
</p>

<p>
Eso sí, habrá que cumplir entonces unas normas para garantizar la calidad mínima exigible y de lectura de la información. 
A continuación damos unas reglas visuales sobre cómo deben formarse esas imágenes.
</p>


<ul>

<li>Un documento de una página, debe llenar la pantalla de la cámara de fotos.</li>
<li>Un documento de dos páginas, debe intentar captarse en una única foto, 
poniendo la segunda página a continuación de la primera, y tomando la información en una única pantalla.
</li>
<li>Si el documento tiene más páginas (historia de vida laboral, IRPF, etc.) deben tomarse una foto que llene la pantalla de la cámara por cada página.</li>
<li>Las fotos deben tomarse centradas. Debes evitar el efecto perspectiva.</li>
<li>No pongas una página al lado de otra para tomar la foto.</li>

</ul>
